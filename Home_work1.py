import pandas as pd

# Создание DataFrame с оценками учеников по 5 предметам
data = {
    'Ученик': ['Вася Петров', 'Саша Иванов', 'Света Гаврилова', 'Жора Сидоров', 'Анна Кузнецова', 'Алексей Лебедев',
               'Виктория Морозова', 'Антон Николаев', 'Дмитрий Антонов', 'Мария Смирнова'],
    'Математика': [85, 90, 78, 92, 88, 76, 95, 89, 84, 91],
    'Физика': [80, 85, 79, 88, 86, 77, 90, 83, 81, 87],
    'Химия': [78, 83, 85, 90, 87, 80, 88, 82, 84, 86],
    'Биология': [82, 88, 80, 91, 85, 79, 92, 87, 83, 89],
    'Информатика': [88, 92, 84, 89, 91, 83, 90, 86, 87, 93]
}

df = pd.DataFrame(data)

# Вывод первых нескольких строк DataFrame
print(df.head())

# Вычисление средней оценки по каждому предмету
mean_scores = df.mean(numeric_only=True)
print(f"\nСредние оценки по каждому предмету:\n{mean_scores}")

# Вычисление медианной оценки по каждому предмету
median_scores = df.median(numeric_only=True)
print(f"\nМедианные оценки по каждому предмету:\n{median_scores}")

# Вычисление Q1 и Q3 для оценок по математике
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
print("\nQ1 для оценок по математике:", Q1_math)
print("Q3 для оценок по математике:", Q3_math)

# Вычисление IQR для оценок по математике
IQR_math = Q3_math - Q1_math
print("IQR для оценок по математике:", IQR_math)

# Вычисление стандартного отклонения по каждому предмету
std_dev = df.std(numeric_only=True)
print(f"\nСтандартное отклонение по каждому предмету:\n{std_dev}")
